# 条件运算符

# 大于 >
print(1 > 0)
# 小于 <
print(1 < 0)

# 等于 ==
print(1 == 0)
# 不等于 !=
print(1 != 0)
# 大于等于 >=
print(1 >= 0)
# 小于等于 <=
print(1 <= 0)

a = 1
b = 0
print(a > b)

# 字符串 列表 元组 == !=

a = "sssssss"
b = "sssssss"
print(a == b)
print(a != b)

# 成员判断  列表 元组 字典 字符串
